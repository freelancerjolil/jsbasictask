let points = 80;
// console.log(points);

if (points >=90 && points <=100) {
    console.log("A+")
}
else if (points >=80 && points <=89){
    console.log("B+")
}
else if (points >=70 && points <=79){
    console.log("C+")
}
else if (points >=60 && points <=69){
    console.log("D+")
}
else if (points >=0 && points <=59){
    console.log("Fail")
}
else {
    console.log("Invalid Number")
}


